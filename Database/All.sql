    INDEX (SINGLE_ID),
    INDEX (ACCOUNT_ID),
    INDEX (TRANSACTION_ID),
    INDEX (STATUS)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ============================================
-- TOURNAMENTS & POINTS
